#!/usr/bin/env node
import { createRequire } from 'node:module';
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

function parseCairoMatchConstants(source, functionName, expected) {
  const start = source.indexOf(`pub fn ${functionName}`);
  if (start === -1) {
    throw new Error(`missing ${functionName} in existing Cairo Poseidon constants`);
  }
  const nextFunction = source.indexOf('\npub fn ', start + 1);
  const block = source.slice(start, nextFunction === -1 ? source.length : nextFunction);
  const values = [...block.matchAll(/\b\d+\s*=>\s*(0x[0-9a-f]+)/g)].map((match) => BigInt(match[1]));
  if (values.length !== expected) {
    throw new Error(`${functionName} expected ${expected} constants, found ${values.length}`);
  }
  return values;
}

function chunkMatrix(values, width) {
  const rows = [];
  for (let i = 0; i < values.length; i += width) {
    rows.push(values.slice(i, i + width));
  }
  return rows;
}

function loadConstants() {
  try {
    const requireFromLocal = createRequire(new URL('../package.json', import.meta.url));
    const { C, M } = requireFromLocal('circom/src/poseidon_constants.json');
    return { C, M, source: 'optional local circom package' };
  } catch {
    const source = readFileSync(new URL('../cairo/src/poseidon_constants.cairo', import.meta.url), 'utf8');
    const c3 = parseCairoMatchConstants(source, 'poseidon_t3_c', 195);
    const c4 = parseCairoMatchConstants(source, 'poseidon_t4_c', 256);
    const c6 = parseCairoMatchConstants(source, 'poseidon_t6_c', 408);
    const m3 = parseCairoMatchConstants(source, 'poseidon_t3_m', 9);
    const m4 = parseCairoMatchConstants(source, 'poseidon_t4_m', 16);
    const m6 = parseCairoMatchConstants(source, 'poseidon_t6_m', 36);
    return {
      C: [[], c3, c4, [], c6],
      M: [[], chunkMatrix(m3, 3), chunkMatrix(m4, 4), [], chunkMatrix(m6, 6)],
      source: 'checked-in cairo/src/poseidon_constants.cairo',
    };
  }
}

const { C, M, source } = loadConstants();

function parseConstant(value) {
  return BigInt(value).toString(16);
}

function cairoValue(value) {
  return `0x${parseConstant(value)}`;
}

function matchFunction(name, max, values, fallbackName) {
  const arms = values.map((value, idx) => `        ${idx} => ${cairoValue(value)},`).join('\n');
  return `pub fn ${name}(idx: u32) -> u256 {
    assert(idx < ${max}, '${fallbackName}');
    match idx {
${arms}
        _ => 0,
    }
}
`;
}

function matrixFunction(name, width, matrix, fallbackName) {
  const flat = matrix.flat();
  return `pub fn ${name}(row: u32, col: u32) -> u256 {
    assert(row < ${width}, '${fallbackName}_ROW');
    assert(col < ${width}, '${fallbackName}_COL');
    let idx = row * ${width} + col;
${matchFunction(`${name}_flat`, flat.length, flat, `${fallbackName}_IDX`)
  .split('\n')
  .slice(1)
  .join('\n')}
`;
}

function generate() {
  const t3Constants = C[1];
  const t4Constants = C[2];
  const t6Constants = C[4];
  const t3Mds = M[1];
  const t4Mds = M[2];
  const t6Mds = M[4];

  return `// Generated by tools/generate-poseidon-cairo-constants.mjs.
// Source: ${source}.
// Covers the Circom-compatible Poseidon arities used by AMACI:
// - t = 3 for HashLeftRight / poseidon2
// - t = 4 for PoseidonDecryptWithoutCheck
// - t = 6 for Hasher5 / poseidon5

pub const POSEIDON_T3_WIDTH: u32 = 3;
pub const POSEIDON_T3_ROUND_CONSTANTS: u32 = ${t3Constants.length};
pub const POSEIDON_T3_MDS_VALUES: u32 = ${t3Mds.length * t3Mds.length};
pub const POSEIDON_T4_WIDTH: u32 = 4;
pub const POSEIDON_T4_ROUND_CONSTANTS: u32 = ${t4Constants.length};
pub const POSEIDON_T4_MDS_VALUES: u32 = ${t4Mds.length * t4Mds.length};
pub const POSEIDON_T6_WIDTH: u32 = 6;
pub const POSEIDON_T6_ROUND_CONSTANTS: u32 = ${t6Constants.length};
pub const POSEIDON_T6_MDS_VALUES: u32 = ${t6Mds.length * t6Mds.length};

${matchFunction('poseidon_t3_c', t3Constants.length, t3Constants, 'BAD_T3_C')}
${matrixFunction('poseidon_t3_m', t3Mds.length, t3Mds, 'BAD_T3_M')}
${matchFunction('poseidon_t4_c', t4Constants.length, t4Constants, 'BAD_T4_C')}
${matrixFunction('poseidon_t4_m', t4Mds.length, t4Mds, 'BAD_T4_M')}
${matchFunction('poseidon_t6_c', t6Constants.length, t6Constants, 'BAD_T6_C')}
${matrixFunction('poseidon_t6_m', t6Mds.length, t6Mds, 'BAD_T6_M')}
`;
}

const outputPath = resolve(process.argv[2] ?? 'cairo/src/poseidon_constants.cairo');
writeFileSync(outputPath, generate());
process.stdout.write(`${outputPath}\n`);
